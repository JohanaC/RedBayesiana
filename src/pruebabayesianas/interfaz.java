    private void btn2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn2ActionPerformed
        // TODO add your handling code here:
        JFileChooser fc = new JFileChooser();
        //Mostrar la ventana para abrir archivo y recoger la respuesta
        //En el parámetro del showOpenDialog se indica la ventana
        //  al que estará asociado. Con el valor this se asocia a la
        //  ventana que la abre.
        int respuesta = fc.showOpenDialog(this);
        //Comprobar si se ha pulsado Aceptar
        if (respuesta == JFileChooser.APPROVE_OPTION){
            //Crear un objeto File con el archivo elegido
                File archivoElegido = fc.getSelectedFile();
            //Mostrar el nombre del archvivo en un campo de texto
            txtNombre.setText("");
            txtNombre.setText(archivoElegido.getName());
            txtNombre.setEditable(false);
            String y=archivoElegido.getAbsolutePath();///obtengo la ruta del archivo elegido para enviarlo como parametro
            yy=y;
            new PruebaBayesianas();
            PruebaBayesianas a = new PruebaBayesianas();
            for(int i=0; i<25; i++){
                nodos.setValueAt("", i, 0);
            }
            int i = 0;
            nodos.show(true);
            campoResul.setVisible(true);
            campoResul.setText("");
            informacion.setText("");
            informacion2.setText("");
            for (modelo x : a.obtenerNodos(y)) {
                nodos.setValueAt(x.getNombre(), i, 0);
                campoResul.setText(String.valueOf(x.getResul()));
                i++;
                
            }
            campoResul.setEditable(false);
           
        }
    }//GEN-LAST:event_btn2ActionPerformed

