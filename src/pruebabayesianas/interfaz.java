    private void nodosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_nodosMouseClicked

        PruebaBayesianas a = new PruebaBayesianas();
        informacion.setText(String.valueOf(a.informacion(String.valueOf(nodos.getValueAt(nodos.getSelectedRow(),nodos.getSelectedColumn())), yy)));
        informacion2.setText(String.valueOf(a.devInf(String.valueOf(nodos.getValueAt(nodos.getSelectedRow(),nodos.getSelectedColumn())), yy)).concat("\nRelevancia: ").concat(String.valueOf(a.relevancia(String.valueOf(nodos.getValueAt(nodos.getSelectedRow(),nodos.getSelectedColumn())), yy))));
       
        
        
        informacion.setEditable(false);
        informacion2.setEditable(false);
    }//GEN-LAST:event_nodosMouseClicked

